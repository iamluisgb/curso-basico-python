# -------------------------------------
# OPERADORES NUMÉRICOS EN PYTHON
# -------------------------------------

print("---OPERADORES BÁSICOS---")

# suma
a = 10
b = 5

suma = a + b
print(f"Suma: {a} + {b} = {suma}")

# Resta (-)
resta = a - b
print(f"Resta: {a} - {b} = {resta}")

# Multiplicación (*)
multiplicacion = a * b
print(f"Multiplicación: {a} * {b} = {multiplicacion}")

# División (/)
division = a / b
print(f"División: {a} / {b} = {division}")

print("\n=== OPERADORES ESPECIALES ===")

# División entera (//)
# Devuelve solo la parte entera del resultado
division_entera = a // b
print(f"División entera: {a} // {b} = {division_entera}")

# Módulo (%)
# Devuelve el resto de la división
modulo = a % b
print(f"Módulo (resto): {a} % {b} = {modulo}")

# Exponente (**)
# Eleva un número a una potencia
exponente = a ** 2
print(f"Exponente: {a} ** 2 = {exponente}")